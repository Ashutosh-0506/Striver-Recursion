#include <bits/stdc++.h>
using namespace ::std;

void rev(int arr[], int r, int l)
{
    if (l >= r)
    {
        return;
    }
    else
    {
        swap(arr[r], arr[l]);
        rev(arr, r - 1, l + 1);
    }
}
int main()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    rev(arr, n-1,0);
    cout << "Reversed array is" << endl;
    for (int i = 0; i < n; i++)
    {
        cout << arr[i] << " ";
    }
    cout << endl;
    return 0;
}